<template>
  <div
    class="
      flex flex-col
      w-11/12
      p-3
      overflow-y-auto
      rounded-lg
      shadow-lg
      relative
    "
    :style="styles"
    style="max-height: 85vh"
  >
    <div class="grid grid-cols-8">
      <img
        :src="generalInfo.avatar"
        v-if="generalInfo.avatar"
        class="col-span-2 w-10 h-10 rounded-full mr-3"
      />

      <div class="col-span-5">
        <div class="text-xs">{{ generalInfo.name }}</div>
        <div v-if="generalInfo.url" class="text-bodysm">
          <a :href="generalInfo.url">{{ generalInfo.url }}</a>
        </div>
      </div>

      <font-awesome-icon
        class="col-span-1 justify-self-end cursor-pointer rounded-full m-1"
        @click="closeModalAndResetState"
        icon="times"
      />
    </div>

    <div class="p-1 border-b border-crisiscleanup-dark-300">
      <div class="grid grid-cols-2">
        <div class="col-span-1">
          <div class="flex text-crisiscleanup-dark-300 text-bodyxsm">
            {{ $t('pewPew.type') }}
            <ccu-icon
              v-tooltip="{
                content: $t('pewPew.type_description'),
                trigger: 'click',
                classes: 'interactive-tooltip w-auto',
              }"
              v-bind="helpTooltipAttrs"
            />
          </div>
          <div v-if="generalInfo.organization">
            {{
              $t(
                generalInfo.organization.type_t
                  ? generalInfo.organization.type_t
                  : 'pewPew.unknown',
              )
            }}
          </div>
        </div>
        <div class="col-span-1">
          <div class="flex text-crisiscleanup-dark-300 text-bodyxsm">
            {{ $t('pewPew.role') }}
            <ccu-icon
              v-tooltip="{
                content: $t('pewPew.role_description'),
                trigger: 'click',
                classes: 'interactive-tooltip w-auto',
              }"
              v-bind="helpTooltipAttrs"
            />
          </div>
          <div>
            {{
              $t(
                generalInfo.approved_roles
                  ? getRoleNames(generalInfo.approved_roles)
                  : 'pewPew.unknown',
              )
            }}
          </div>
        </div>
      </div>
      <div class="grid grid-cols-4 gap-1 mt-2">
        <div class="col-span-1">
          <div class="flex text-crisiscleanup-dark-300 truncate text-bodyxsm">
            {{ $t('pewPew.incidents') }}
            <ccu-icon
              v-tooltip="{
                content: $t('pewPew.incidents_description'),
                trigger: 'click',
                classes: 'interactive-tooltip w-auto',
              }"
              v-bind="helpTooltipAttrs"
            />
          </div>
          <div>
            {{ $t(generalInfo.statistics ? generalInfo.statistics.length : 0) }}
          </div>
        </div>
        <div class="col-span-1">
          <div class="flex text-crisiscleanup-dark-300 truncate text-bodyxsm">
            {{ $t('pewPew.cases_upper') }}
            <ccu-icon
              v-tooltip="{
                content: $t('pewPew.cases_description'),
                trigger: 'click',
                classes: 'interactive-tooltip w-auto',
              }"
              v-bind="helpTooltipAttrs"
            />
          </div>
          <div>
            {{ getTotalCases() || 0 }}
          </div>
        </div>
        <div class="col-span-1">
          <div class="flex text-crisiscleanup-dark-300 truncate text-bodyxsm">
            {{ $t('pewPew.calls') }}
            <ccu-icon
              v-tooltip="{
                content: $t('pewPew.call_description'),
                trigger: 'click',
                classes: 'interactive-tooltip w-auto',
              }"
              v-bind="helpTooltipAttrs"
            />
          </div>
          <div>
            {{ getTotalCalls() || 0 }}
          </div>
        </div>
        <div class="col-span-1">
          <div class="flex text-crisiscleanup-dark-300 truncate text-bodyxsm">
            {{ $t('pewPew.value') }}
            <ccu-icon
              v-tooltip="{
                content: $t('pewPew.value_description'),
                trigger: 'click',
                classes: 'interactive-tooltip w-auto',
              }"
              v-bind="helpTooltipAttrs"
            />
          </div>
          <div>
            {{ nFormatter(getTotalValue()) }}
          </div>
        </div>
      </div>
    </div>

    <section class="incidents-section">
      <div
        class="flex justify-between items-center cursor-pointer"
        @click="isIncidentHidden = !isIncidentHidden"
      >
        <span>{{ $t('pewPew.incidents') }}</span>
        <font-awesome-icon
          class="transition duration-500 transform"
          :class="isIncidentHidden ? 'rotate-0' : 'rotate-180'"
          icon="chevron-down"
          size="lg"
        />
      </div>

      <transition name="fade">
        <Table
          v-show="!isIncidentHidden"
          :columns="incidentTable.columns"
          :data="generalInfo.statistics"
          :body-style="{ maxHeight: '40vh', ...styles }"
          :header-style="{ ...styles }"
          :row-style="{ backgroundColor: 'unset' }"
          :key="generalInfo.id"
        >
          <template #name="slotProps">
            {{ slotProps.item.name }}
          </template>
          <template #calls="slotProps">
            <span class="w-full flex justify-center">
              {{ nFormatter(slotProps.item.calls || 0)
              }}<span class="pew-pew-blue">*</span>
            </span>
          </template>
          <template #commercial_value="slotProps">
            <span class="w-full flex justify-end">
              ${{ nFormatter(slotProps.item.commercial_value || 0)
              }}<span class="pew-pew-blue">*</span>
            </span>
          </template>
          <template #reported_count="slotProps">
            <CaseDonutChart
              class="w-8 h-8"
              :chart-id="`case-donut-org-modal-${slotProps.item.id}`"
              :chart-data="{
                reportedCases: slotProps.item.reported_count || 0,
                claimedCases:
                  (slotProps.item.claimed_count || 0) -
                  (slotProps.item.closed_count || 0),
                completedCases: slotProps.item.closed_count || 0,
              }"
              :bg-color="styles.backgroundColor"
              :margin-all="5"
            />
          </template>
        </Table>
      </transition>
    </section>

    <section class="capabilities-section">
      <div
        class="flex justify-between items-center cursor-pointer"
        @click="isCapabilityHidden = !isCapabilityHidden"
      >
        <span>{{ $t('pewPew.capabilities') }}</span>
        <font-awesome-icon
          class="transition duration-500 transform"
          :class="isCapabilityHidden ? 'rotate-0' : 'rotate-180'"
          icon="chevron-down"
          size="lg"
        />
      </div>

      <transition name="fade">
        <Capability
          v-show="!isCapabilityHidden"
          :capabilities="capabilities"
          :organization-capabilities="generalInfo.capabilities"
        />
      </transition>
    </section>
    <div
      v-if="isLoading"
      class="absolute inset-0 opacity-75 flex items-center justify-center"
    >
      <spinner color="#d3d3d3" />
    </div>
  </div>
</template>

<script>
import _ from 'lodash';
import Capability from '@/components/Capability.vue';
import { makeTableColumns } from '@/utils/table';
import CaseDonutChart from '@/components/charts/CaseDonutChart';
import { nFormatter } from '@/utils/helpers';
import Table from '@/components/Table';

export default {
  name: 'OrganizationActivityModal',
  components: { Table, CaseDonutChart, Capability },
  props: {
    generalInfo: {
      type: Object,
      default: null,
    },
    styles: {
      type: Object,
      default: null,
    },
    incidents: {
      type: Array,
      default: () => [],
    },
    capability: {
      type: Array,
      default: () => [],
    },
    isLoading: {
      type: Boolean,
      default: false,
    },
  },
  async mounted() {
    const capabilities = await this.$http.get(
      `${process.env.VUE_APP_API_BASE_URL}/organization_capabilities?limit=200`,
    );
    this.capabilities = capabilities.data.results;

    const roles = await this.$http.get(
      `${process.env.VUE_APP_API_BASE_URL}/organization_roles`,
    );
    this.roles = roles.data.results;
  },
  data() {
    return {
      isIncidentHidden: true,
      isCapabilityHidden: true,
      nFormatter,
      capabilities: [],
      roles: [],
    };
  },
  methods: {
    getTotalValue() {
      return _.sumBy(
        this.generalInfo.statistics,
        (stat) => stat.commercial_value || 0,
      );
    },
    getTotalCalls() {
      return _.sumBy(this.generalInfo.statistics, (stat) => stat.calls || 0);
    },
    getTotalCases() {
      return _.sumBy(
        this.generalInfo.statistics,
        (stat) =>
          (stat.reported_count || 0) +
          ((stat.claimed_count || 0) - (stat.closed_count || 0)) +
          (stat.closed_count || 0),
      );
    },
    getRoleNames(roleIds) {
      return _.join(
        _.map(
          roleIds,
          (id) => _.filter(this.roles, (role) => role.id === id)[0].name_t,
        ),
        ', ',
      );
    },
    closeModalAndResetState() {
      this.$emit('close');
      this.isIncidentHidden = true;
      this.isCapabilityHidden = true;
    },
  },
  computed: {
    helpTooltipAttrs() {
      return {
        invertColor: true,
        alt: this.$t('actions.help_alt'),
        type: 'help',
        size: 'medium',
      };
    },
    incidentTable() {
      const columns = makeTableColumns([
        ['name', '40%'],
        ['reported_count', '20%', 'Cases'],
        ['calls', '20%', 'Calls'],
        ['commercial_value', '20%', 'Value'],
      ]);
      columns.forEach((column) => {
        column.titleClass = 'small-font';
        column.class = 'small-font';
        column.style = {
          border: 0,
        };
        column.headerStyle = {
          border: 0,
        };
      });
      return {
        columns,
      };
    },
  },
};
</script>
